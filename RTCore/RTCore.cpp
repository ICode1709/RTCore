PVOID CRTCore::GetPhysicalAddress(ULONG_PTR DirectoryTableBase, PVOID VirtualAddress)
{
	ULONG_PTR Cr3 = (ULONG_PTR)DirectoryTableBase & ~0xF;

	ULONG_PTR VA = (ULONG_PTR)VirtualAddress;

	ULONG64 pageOffset = VA & ~(~0ul << 12);
	ULONG64 pte = ((VA >> 12) & (0x1ffll));
	ULONG64 pt = ((VA >> 21) & (0x1ffll));
	ULONG64 pd = ((VA >> 30) & (0x1ffll));
	ULONG64 pdp = ((VA >> 39) & (0x1ffll));

	ULONG64 pdpe = 0;
	if ((ReadPhysical((PVOID)(Cr3 + 8 * pdp), &pdpe, sizeof(pdpe)) == FALSE) || ~pdpe & 1)
		return 0;

	ULONG64 pde = 0;
	if ((ReadPhysical((PVOID)((pdpe & 0xFFFFFF000) + 8 * pd), &pde, sizeof(pde)) == FALSE) || ~pde & 1)
		return 0;

	if (pde & 0x80)
		return (PVOID)((pde & (~0ull << 42 >> 12)) + (VA & ~(~0ull << 30)));

	ULONG64 pteAddr = 0;
	if ((ReadPhysical((PVOID)((pde & 0xFFFFFF000) + 8 * pt), &pteAddr, sizeof(pteAddr)) == FALSE) || ~pteAddr & 1)
		return 0;

	if (pteAddr & 0x80)
		return (PVOID)((pteAddr & 0xFFFFFF000) + (VA & ~(~0ull << 21)));

	VA = 0;
	if (ReadPhysical((PVOID)((pteAddr & 0xFFFFFF000) + 8 * pte), &VA, sizeof(VA)) == FALSE)
		return 0;

	VA &= 0xFFFFFF000;
	if (!VA)
		return 0;

	return (PVOID)(VA + pageOffset);
}